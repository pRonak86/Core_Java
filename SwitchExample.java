public class SwitchExample {
    public static void main(String[] args) {

        int a =20;
        switch (a)
        {
            case 1:
                System.out.println("First Case is Matched");
                break;
            case 2:
                System.out.println("Second Case is Matched");
                break;
            case 3:
                System.out.println("Third Case is Matched");
                break;
            case 4:
                System.out.println("Fourth Case is Matched");
                break;
            case 5:
                System.out.println("Fifth Case is Matched");
                break;
            default:
                System.out.println("Not match found");
        }
    }
}
